#include "ft_printf.h"

char	ft_itoc_base(int c, int base)
{
	if (c > base)
		return (c %= base);
	if (c < 10)
		return (c + '0');
	if (c < 36)
		return (c - 10 + 'a');
	if (c < 62)
		return (c - 36 + 'A');
	return ('\0');
}
